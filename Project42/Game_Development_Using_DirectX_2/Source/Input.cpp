#include "DInput.h"


///////////////////////////////////////////////////////////
//DInput Constructor
///////////////////////////////////////////////////////////

CInput::CInput(void)
{
	lpdi = NULL;
}

///////////////////////////////////////////////////////////
//CInput Destructor
///////////////////////////////////////////////////////////
CInput::~CInput(void)
{
	RELEASE (lpdi); 
}

///////////////////////////////////////////////////////////
//CInput CreateInput
///////////////////////////////////////////////////////////
HRESULT CInput::CreateInput(void *hinst)
{
	// first create direct input object//
	return DirectInputCreate((HINSTANCE) hinst, DIRECTINPUT_VERSION, &lpdi, NULL);
}
