const int CMD5::HASH_LENGTH = 16;
const int CMD5::DATA_LENGTH = 64;

CMD5::CMD5() : CMessageDigest()
{
	BufferSetSize(Data_Length());
	MD5_Init();
	Reset();
}

void CMD5::MD5_Init()
{
	Digest	= new CUIntArray();
	Digest->SetSize(HASH_LENGTH/4);
	Data	= new CUIntArray();
	Data->SetSize(DATA_LENGTH/4);
	Temp	= new CByteArray();
	Temp->SetSize(DATA_LENGTH);

}


CMD5::~CMD5()
{

}

void CMD5::MD_Reset()
{
	Digest->SetAt(0,0x67452301);
	Digest->SetAt(1,0xefcdab89);
	Digest->SetAt(2,0x98badcfe);
	Digest->SetAt(3,0x10325476);
}



void CMD5::MD_Transform()
{
	byte2int(Data, 0, Buffer(), 0, DATA_LENGTH/4);
	Transform(Data);
	
}

void CMD5::byte2int(CUIntArray* dst, int dst_off, CByteArray* src, int src_off, int len)
{
	while (len-- > 0)
	{
		// Little endian
		dst->SetAt(dst_off++, ((src->GetAt(src_off++) & 0xFF) | ((src->GetAt(src_off++) << 8) & 0xFF00)
				| ((src->GetAt(src_off++) << 16) & 0xFF0000) | ((src->GetAt(src_off++) << 24))));
	}
	
}



