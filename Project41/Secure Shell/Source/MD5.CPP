UINT CMD5::F(UINT x, UINT y, UINT z)
{
	return (z^(x & (y^z)));
}

UINT CMD5::G(UINT x, UINT y, UINT z)
{
	return (y^(z & (x^y)));
}	

UINT CMD5::H(UINT x, UINT y, UINT z)
{
	return (x^y^z);
}

UINT CMD5::I(UINT x, UINT y, UINT z)
{
	return (y^(x | ~z));
}

// ROTATE_LEFT rotates x left n bits
#define ROTATE_LEFT(x,n) (((x) << (n)) | ((x) >> (32-(n))))

UINT CMD5::FF(UINT a, UINT b, UINT c, UINT d, UINT k, UINT s, UINT t)
{
			a += k+t+F(b,c,d);
			a = ROTATE_LEFT((a),(s));
			return a+b;

}

UINT CMD5::GG(UINT a, UINT b, UINT c, UINT d, UINT k, UINT s, UINT t)
{
			a += k+t+G(b,c,d);
			a = ROTATE_LEFT((a),(s));
			return a+b;

}

UINT CMD5::HH(UINT a, UINT b, UINT c, UINT d, UINT k, UINT s, UINT t)
{
			a += k+t+H(b,c,d);
			a = ROTATE_LEFT((a),(s));
			return a+b;

}


UINT CMD5::II(UINT a, UINT b, UINT c, UINT d, UINT k, UINT s, UINT t)
{
			a += k+t+I(b,c,d);
			a = ROTATE_LEFT((a),(s));
			return a+b;

}



CByteArray* CMD5::MD_Digest()
{
	int pos = BufferOffset();
	if ( pos != 0)  
		for (int i=0;i<pos;i++)
			Temp->SetAt(i,Buffer()->GetAt(i));
	Temp->SetAt(pos++,-128);

	// if over 448 bits write zero to end block
	if ( pos > (DATA_LENGTH-8))
	{
		while (pos < DATA_LENGTH) Temp->SetAt(pos++,0);
		byte2int(Data,0,Temp,0,DATA_LENGTH/4);
		Transform(Data);
		pos = 0;
	}
	while (pos < (DATA_LENGTH-8)) Temp->SetAt(pos++,0);
	byte2int(Data, 0, Temp, 0, (DATA_LENGTH/4)-2);

	// Little endian
	Data->SetAt(14,(UINT)BitCount());
	Data->SetAt(15,(UINT)((BitCount() & 0x0000)>>32));

	Transform(Data);

	CByteArray* Buf = new CByteArray();
	Buf->SetSize(HASH_LENGTH);

	// Little endian
	int off = 0;
	for (int i=0;i<HASH_LENGTH/4;++i) {
		UINT d = Digest->GetAt(i);
		Buf->SetAt(off++,(BYTE) d);
		Buf->SetAt(off++,(BYTE)((d&0xffffff00)>>8));
		Buf->SetAt(off++,(BYTE)((d&0xffff0000)>>16));
		Buf->SetAt(off++,(BYTE)((d&0xff000000)>>24));
	}
	return Buf;

}

